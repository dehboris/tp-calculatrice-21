package com.calculator;

public class Operations {
    public static int somme(int a, int b){
        return a + b;
    }

    public static int prod(int a, int b){
        return a * b;
    }

    public static int diff(int a, int b){
        return a - b;
    }

    public static int div(int a, int b){
        return a / b;
    }
}
